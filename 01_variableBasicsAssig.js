// Number type variables
var maths = 95;
console.log("Mathematics Marks   :" , maths, "               Type :", typeof maths);
var science = 92;
console.log("Science Marks       :" , science, "               Type :", typeof science);
var english = 85;
console.log("English Marks       :" , english, "               Type :", typeof english);

// String type variables
var mathRemarks = "Excellent!";
console.log("Mathematics Remarks :", mathRemarks, "       Type :", typeof mathRemarks);
var scienceRemarks = "Good! Keep it up.";
console.log("Science Remarks     :", scienceRemarks, "Type :", typeof scienceRemarks);
var englishRemarks = "Can do better.";
console.log("English Remarks     :", englishRemarks, "   Type :", typeof englishRemarks);

// Boolean type variables
var isActive = true;
console.log("Is student Active   :", isActive, "             Type :", typeof isActive);
var isObidient = true;
console.log("Is student Obidient :", isObidient, "             Type :", typeof isObidient);
var isFriendly = false;
console.log("Is student Friendly :", isFriendly, "            Type :", typeof isFriendly);

